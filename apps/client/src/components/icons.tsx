export type NavFamily = 'core' | 'finance' | 'hr' | 'operations' | 'system';

export function FamilyIcon({ family, ...props }: IconProps & { family: NavFamily | string }) {
  switch (family) {
    case 'core':
      return <IconBuilding {...props} />;
    case 'finance':
      return <IconFinance {...props} />;
    case 'hr':
      return <IconHr {...props} />;
    case 'operations':
      return <IconOperations {...props} />;
    case 'system':
      return <IconSystem {...props} />;
    default:
      return <IconDashboard {...props} />;
  }
}
